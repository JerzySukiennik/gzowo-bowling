let currentLang = "pl";
try {
  const savedLang = localStorage.getItem("gzowo.lang");
  if (savedLang === "pl" || savedLang === "en") currentLang = savedLang;
} catch (e) {}
const listeners = new Set();

export function setLang(lang) {
  if (lang !== "pl" && lang !== "en") return;
  if (lang === currentLang) return;
  currentLang = lang;
  try { localStorage.setItem("gzowo.lang", lang); } catch (e) {}
  for (const cb of [...listeners]) {
    try { cb(currentLang); } catch (e) { console.warn("i18n listener failed", e); }
  }
}

export function getLang() {
  return currentLang;
}

export function onLangChange(cb) {
  if (typeof cb !== "function") return () => {};
  listeners.add(cb);
  return () => listeners.delete(cb);
}

export function t(key, vars) {
  const other = currentLang === "pl" ? "en" : "pl";
  let str = DICT[currentLang][key];
  if (str === undefined) str = DICT[other][key];
  if (str === undefined) return key;
  if (vars) {
    for (const k of Object.keys(vars)) {
      str = str.split("{" + k + "}").join(String(vars[k]));
    }
  }
  return str;
}

export function applyI18n(root) {
  const scope = root || document;
  try {
    scope.querySelectorAll("[data-i18n]").forEach((el) => {
      el.textContent = t(el.getAttribute("data-i18n"));
    });
    scope.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      el.setAttribute("placeholder", t(el.getAttribute("data-i18n-placeholder")));
    });
    scope.querySelectorAll("[data-i18n-title]").forEach((el) => {
      el.setAttribute("title", t(el.getAttribute("data-i18n-title")));
    });
    scope.querySelectorAll("[data-i18n-aria]").forEach((el) => {
      el.setAttribute("aria-label", t(el.getAttribute("data-i18n-aria")));
    });
  } catch (e) {
    console.warn("applyI18n failed", e);
  }
}
